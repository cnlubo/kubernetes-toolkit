#!/bin/bash
# shellcheck disable=SC2034
# Color Palette
RESET='\033[0m'
BOLD='\033[1m'
## Foreground
BLACK='\033[38;5;0m'
RED='\033[38;5;1m'
GREEN='\033[38;5;2m'
YELLOW='\033[38;5;3m'
BLUE='\033[38;5;4m'
MAGENTA='\033[38;5;5m'
CYAN='\033[38;5;6m'
WHITE='\033[38;5;7m'
## Background
ON_BLACK='\033[48;5;0m'
ON_RED='\033[48;5;1m'
ON_GREEN='\033[48;5;2m'
ON_YELLOW='\033[48;5;3m'
ON_BLUE='\033[48;5;4m'
ON_MAGENTA='\033[48;5;5m'
ON_CYAN='\033[48;5;6m'
ON_WHITE='\033[48;5;7m'

MODULE="$(basename $0)"

stderr_print() {
    printf "%b\\n" "${*}" >&2
}
log() {
stderr_print "[${BLUE}${MODULE} ${MAGENTA}$(date "+%Y-%m-%d %H:%M:%S ")${RESET}] ${*}"
}
info() {

  log "${GREEN}INFO ${RESET} ==> ${*}"
}
warn() {

  log "${YELLOW}WARN ${RESET} ==> ${*}"
}
error() {
  log "${RED}ERROR${RESET} ==> ${*}"
}

info "delete secrets k8s-dashboard-secret ..... "
kubectl delete secrets k8s-dashboard-secret -n kubernetes-dashboard

info "delete secrets kubernetes-dashboard-key-holder ..... "
kubectl delete secrets -n  kubernetes-dashboard kubernetes-dashboard-key-holder

info "uninstall dashboard ingress-nginx ..... "
cd /opt/k8s/addons/dashboard/manual/ingress || exit
kubectl delete -f ingress-dashboard-tls.yaml

info "uninstall dashboard ..... "
cd /opt/k8s/addons/dashboard/manual || exit
kubectl delete -f dashboard-admin-user.yaml
kubectl delete -f kubernetes-dashboard.yaml
